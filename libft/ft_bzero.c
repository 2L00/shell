#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	i = 0;
	if (!s && !n)
		return ;
	while (i < n)
	{
		ptr[i] = 0;
		i++;
	}
}
